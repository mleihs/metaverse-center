/** Maps SimulationTheme types to suggested preset names. */
export function getPresetForTheme(theme: SimulationTheme): ThemePresetName {
  switch (theme) {
    case 'dystopian':
      return 'cyberpunk';
    case 'utopian':
      return 'solarpunk';
    case 'fantasy':
      return 'sunless-sea';
    case 'scifi':
      return 'cyberpunk';
    case 'historical':
      return 'nordic-noir';
    default:
      return 'brutalist';
  }
}

